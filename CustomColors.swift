import UIKit

extension UIColor {
    
    class func maroony() -> UIColor{
        return UIColor(red:0.55, green:0.00, blue:0.11, alpha:1.0)
    }
    
    class func dark() -> UIColor{
        return UIColor(red:0.25, green:0.25, blue:0.25, alpha:1.0)
    }
    
    class func sand1() -> UIColor{
        return UIColor(red:0.98, green:0.96, blue:0.89, alpha:1.0)
    }
    
    class func sand2() -> UIColor{
        return UIColor(red:0.74, green:0.73, blue:0.60, alpha:1.0)
    }
    
    class func sand3() -> UIColor{
        return UIColor(red:0.90, green:0.90, blue:0.81, alpha:1.0)
    }
    
}